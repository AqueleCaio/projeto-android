function clickContent(){
    if (hide_content.style.display == 'block') {
        hide_content.style.display = 'none'
    } else {
        hide_content.style.display = 'block'
    }
}

function showMenu(){
    if (imenu.style.display == 'block'){
        imenu.style.display = 'none'
    } else {
        imenu.style.display = 'block'
    }
}